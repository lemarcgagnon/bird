function page_supported_lang(string $lang): ?string
{
    $lang = strtolower(substr(trim($lang), 0, 2));
    return in_array($lang, ['fr', 'en'], true) ? $lang : null;
}

function page_lang(): string
{
    static $lang = null;
    if ($lang !== null) {
        return $lang;
    }

    $queryLang = page_supported_lang((string) ($_GET['lang'] ?? ''));
    if ($queryLang !== null) {
        $lang = $queryLang;
        setcookie('nichoir_lang', $lang, [
            'expires' => time() + 31536000,
            'path' => '/',
            'samesite' => 'Lax',
        ]);
        return $lang;
    }

    $cookieLang = page_supported_lang((string) ($_COOKIE['nichoir_lang'] ?? ''));
    if ($cookieLang !== null) {
        $lang = $cookieLang;
        return $lang;
    }

    $accept = strtolower((string) ($_SERVER['HTTP_ACCEPT_LANGUAGE'] ?? ''));
    $lang = str_starts_with($accept, 'en') ? 'en' : 'fr';
    return $lang;
}

