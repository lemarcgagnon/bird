function page_tv(string $key, array $vars = [], ?string $lang = null): string
{
    $text = page_t($key, $lang);
    foreach ($vars as $name => $value) {
        $text = str_replace('{' . $name . '}', (string) $value, $text);
    }
    return $text;
}

function page_path_with_lang(string $path, ?string $lang = null): string
{
    $lang ??= page_lang();
    $parts = parse_url($path);
    $base = (string) ($parts['path'] ?? '/');
    $query = [];
    if (isset($parts['query'])) {
        parse_str((string) $parts['query'], $query);
    }
    $query['lang'] = $lang;
    $fragment = isset($parts['fragment']) ? '#' . $parts['fragment'] : '';
    return $base . '?' . http_build_query($query) . $fragment;
}

function dev_app_url(?string $lang = null): string
{
    $lang ??= page_lang();
    $host = $_SERVER['HTTP_HOST'] ?? '';
    if (str_starts_with($host, '127.0.0.1:8021') || str_starts_with($host, 'localhost:8021')) {
        return 'http://127.0.0.1:8016/app/index.html?lang='
            . rawurlencode($lang)
            . '&php_base='
            . rawurlencode('http://127.0.0.1:8021');
    }
    return '/app/index.html?lang=' . rawurlencode($lang);
}
